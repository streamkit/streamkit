package org.mediacenter.resource;

/**
 * Class providing constants for the <code>sling:resourceType</code> nodes used within MediaCenter project's scope.
 */
public class MediaCenterResourceType
{
    /**
     * This value defines the <code>sling:resourceType</code> of a Channel Node.
     */
    public static final String CHANNEL = "mediacenter:channel";

    /**
     * This value defines the <code>sling:resourceType</code> of a Recorded Video Node.
     */
    public static final String VOD = "mediacenter:vod";

    /**
     * This value defines the <code>sling:resourceType</code> of a LIVE Video Node.
     */
    public static final String LIVE = "mediacenter:live";

    /**
     * This value defines the <code>sling:resourceType</code> of an Album Node.
     */
    public static final String ALBUM = "mediacenter:album";

}
